package com.sample;


public class PbBiddingRules {

  private long biddingId;
  private String biddingPattern;
  private String biddingInterval;


  public long getBiddingId() {
    return biddingId;
  }

  public void setBiddingId(long biddingId) {
    this.biddingId = biddingId;
  }


  public String getBiddingPattern() {
    return biddingPattern;
  }

  public void setBiddingPattern(String biddingPattern) {
    this.biddingPattern = biddingPattern;
  }


  public String getBiddingInterval() {
    return biddingInterval;
  }

  public void setBiddingInterval(String biddingInterval) {
    this.biddingInterval = biddingInterval;
  }

}
